let a = prompt('Nhập vào số nguyên thứ 1');
let b = prompt('Nhập vào số nguyên thứ 2');
let c = prompt('Nhập vào số nguyên thứ 3');
if(a>b && a>c) {
    alert('Số thứ 1 '+ a +' là số lớn nhất');
}else if(b>a && b>c) {
    alert('Số thứ 2 '+ b +' là số lớn nhất');
}else if(c>a && c>b) {
    alert('Số thứ 3 '+ c +' là số lớn nhất');

}